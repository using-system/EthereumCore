namespace EthereumCore.Models
{
    /// <summary>
    /// Ethereum Settings
    /// </summary>
    public class EthereumSettings
    {
        /// <summary>
        /// Gets or sets the ehtereum account.
        /// </summary>
        /// <value>
        /// The ehtereum account.
        /// </value>
        public string EhtereumAccount { get; set; }

        /// <summary>
        /// Gets or sets the ehtereum password.
        /// </summary>
        /// <value>
        /// The ehtereum password.
        /// </value>
        public string EhtereumPassword { get; set; }

        /// <summary>
        /// Gets or sets the storage key.
        /// </summary>
        /// <value>
        /// The storage key.
        /// </value>
        public string StorageKey { get; set; }

        /// <summary>
        /// Gets or sets the storage account.
        /// </summary>
        /// <value>
        /// The storage account.
        /// </value>
        public string StorageAccount { get; set; }
    }
}
